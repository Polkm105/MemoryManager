MemoryPage::MemoryPage(void) : 
                       mPtr(nullptr),
                       mSize(0)
{
}

MemoryPage::MemoryPage(void* ptr, size_t size) :
                       mPtr(ptr),
                       mSize(size)
{
}

unsigned int MemoryPage::Size(void) const
{
  return mSize;
}

const void* MemoryPage::Ptr(void) const
{
  return mPtr;
}

void MemoryPage::Destroy(void)
{
  free(mPtr);
  mPtr = NULL;
}

